create or replace function ck_fuel_subtype_consistency() returns trigger
language plpgsql as $$
declare v_parent smallint;
begin
  if new.fuel_subtype_id is null then return new; end if;
  select fuel_type_code into v_parent from fuel_subtype where id = new.fuel_subtype_id;
  if v_parent is null then
    raise exception 'Subtipo de combustível % não existe', new.fuel_subtype_id using errcode = '23503';
  end if;
  if v_parent <> new.fuel_type_code then
    raise exception 'Subtipo % pertence ao combustível código %, mas a linha usa código %',
      new.fuel_subtype_id, v_parent, new.fuel_type_code using errcode = '23514';
  end if;
  return new;
end;
$$;
create trigger trg_supplier_fuel_subtype before insert or update on supplier_fuel
  for each row execute function ck_fuel_subtype_consistency();
