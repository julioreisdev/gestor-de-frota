  ) values (
    v_number, p_date, p_vehicle_id, p_supplier_id, p_service_kind, p_description,
    p_estimated_value, p_responsible_name, p_notes,
    v_plate, v_model, v_dept, v_sup_name,
    v_qr, auth.uid()
  ) returning id into v_auth_id;
  return v_auth_id;
