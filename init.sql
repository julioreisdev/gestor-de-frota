create or replace function emit_authorization(
  p_date date,
  p_vehicle_id uuid,
  p_supplier_id uuid,
  p_fuel_type_code smallint,
  p_quantity numeric,
  p_responsible_name text,
  p_notes text default null,
  p_fuel_subtype_id smallint default null
) returns uuid
language plpgsql security definer set search_path = public, auth as $$
declare
  v_auth_id uuid; v_number text;
  v_price numeric(8,3); v_contract numeric; v_balance numeric;
  v_tank numeric;
  v_user_limit numeric; v_month_used numeric; v_estimated numeric;
  v_plate text; v_model text; v_dept text; v_sup_name text; v_sup_cnpj text; v_qr text;
  v_fuel_desc text; v_sub_desc text;
begin
  select plate, model, tank_capacity into v_plate, v_model, v_tank
    from vehicle where id = p_vehicle_id and deleted_at is null;
  if v_plate is null then raise exception 'Veículo não encontrado'; end if;
  select d.acronym into v_dept
    from vehicle v left join department d on d.id = v.department_id
    where v.id = p_vehicle_id;
  select coalesce(trade_name, legal_name), cnpj into v_sup_name, v_sup_cnpj
    from supplier where id = p_supplier_id;
  if v_sup_name is null then raise exception 'Fornecedor não encontrado'; end if;
  if v_tank is not null and p_quantity > v_tank then
    raise exception 'Quantidade % L excede capacidade do tanque (% L)', p_quantity, v_tank;
  end if;
  if p_fuel_subtype_id is not null then
    select description into v_sub_desc from fuel_subtype where id = p_fuel_subtype_id;
    if v_sub_desc is null then raise exception 'Subtipo de combustível % não existe', p_fuel_subtype_id; end if;
  end if;
  select unit_price, contract_amount, current_balance
    into v_price, v_contract, v_balance
    from supplier_fuel
    where supplier_id = p_supplier_id
      and fuel_type_code = p_fuel_type_code
      and coalesce(fuel_subtype_id, 0::smallint) = coalesce(p_fuel_subtype_id, 0::smallint);
  if v_price is null then
    raise exception 'Fornecedor não vende esse combustível%',
      case when p_fuel_subtype_id is not null then ' (' || v_sub_desc || ')' else '' end;
  end if;
  if v_contract > 0 and p_quantity > v_balance then
    raise exception 'Saldo insuficiente: disponível % L, solicitado % L', v_balance, p_quantity;
  end if;
  v_estimated := p_quantity * v_price;

  -- Limite mensal: soma combustível + manutenção
  select monthly_authorization_limit into v_user_limit
    from app_user where full_name = p_responsible_name and active = true limit 1;
  if v_user_limit is not null and v_user_limit > 0 then
    select coalesce((
      select sum(estimated_total) from fueling_authorization
       where responsible_name = p_responsible_name
         and status in ('emitida','utilizada')
         and date_trunc('month', date) = date_trunc('month', p_date)
         and deleted_at is null
    ), 0) + coalesce((
      select sum(estimated_value) from service_authorization
       where responsible_name = p_responsible_name
         and status in ('emitida','utilizada')
         and date_trunc('month', date) = date_trunc('month', p_date)
         and deleted_at is null
    ), 0)
    into v_month_used;
    if v_month_used + v_estimated > v_user_limit then
      raise exception 'Limite mensal do responsável excedido: limite R$%, usado R$%, nova R$%',
        v_user_limit, v_month_used, v_estimated;
    end if;
  end if;

  v_number := generate_authorization_number(p_date);
  select description into v_fuel_desc from fuel_type where code = p_fuel_type_code;
  v_qr := format(
    E'GERIR FROTA — AUTORIZAÇÃO DE ABASTECIMENTO\n' ||
    E'Nº: %s\nData: %s\nVeículo: %s — %s\nCombustível: %s%s\n' ||
    E'Quantidade: %s L\nValor estimado: R$ %s\n' ||
    E'Fornecedor: %s (CNPJ %s)\nSecretaria: %s\nResponsável: %s\nSituação: EMITIDA',
    v_number, to_char(p_date,'DD/MM/YYYY'), v_plate, v_model,
    v_fuel_desc, case when v_sub_desc is not null then ' / ' || v_sub_desc else '' end,
    to_char(p_quantity,'FM999990D90'), to_char(v_estimated,'FM999990D90'),
    v_sup_name, v_sup_cnpj, coalesce(v_dept,'—'), p_responsible_name
  );
  insert into fueling_authorization (
    number, date, vehicle_id, supplier_id, fuel_type_code, fuel_subtype_id,
    authorized_quantity, unit_price_snapshot, responsible_name, notes,
    vehicle_plate_snapshot, vehicle_model_snapshot,
    department_acronym_snapshot, supplier_trade_name_snapshot, qr_payload, created_by
  ) values (
    v_number, p_date, p_vehicle_id, p_supplier_id, p_fuel_type_code, p_fuel_subtype_id,
    p_quantity, v_price, p_responsible_name, p_notes,
    v_plate, v_model, v_dept, v_sup_name, v_qr, auth.uid()
  ) returning id into v_auth_id;
  if v_contract > 0 then
    update supplier_fuel
      set current_balance = current_balance - p_quantity
      where supplier_id = p_supplier_id
        and fuel_type_code = p_fuel_type_code
        and coalesce(fuel_subtype_id, 0::smallint) = coalesce(p_fuel_subtype_id, 0::smallint);
  end if;
  return v_auth_id;
end;
$$;
revoke all on function emit_authorization(date,uuid,uuid,smallint,numeric,text,text,smallint) from public;
grant execute on function emit_authorization(date,uuid,uuid,smallint,numeric,text,text,smallint) to authenticated;

