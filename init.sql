-- Resolve username → email para login (front chama antes do signInWithPassword).
-- security definer: ignora RLS de auth.users; acessível por anon (pré-login) e authenticated.
create or replace function email_from_username(p_username text)
returns text
language sql security definer set search_path = public, auth as $$
  select u.email
  from app_user au
  join auth.users u on u.id = au.id
  where au.username = lower(p_username) and au.active = true
$$;
revoke all on function email_from_username(text) from public;
grant execute on function email_from_username(text) to anon, authenticated;

-- ---------------------------------------------------------------------------
-- ADMIN: gerenciamento de usuários via RPC (sem Edge Function).
-- ---------------------------------------------------------------------------
create or replace function _require_admin() returns void
language plpgsql security definer set search_path = public as $$
declare v_role user_role; v_active boolean;
begin
  select role, active into v_role, v_active from app_user where id = auth.uid();
  if v_role is null then raise exception 'Sessão inválida' using errcode = '28000'; end if;
  if v_role <> 'admin' or not v_active then
    raise exception 'Apenas administradores ativos podem executar esta ação' using errcode = '42501';
  end if;
end;
$$;
revoke all on function _require_admin() from public;
grant execute on function _require_admin() to authenticated;

create or replace function admin_create_user(
  p_email text, p_password text, p_username text, p_full_name text, p_role user_role,
  p_supplier_id uuid default null, p_access_start date default null,
  p_access_end date default null, p_monthly_authorization_limit numeric default 0
) returns uuid
language plpgsql security definer set search_path = public, auth as $$
declare v_user_id uuid := gen_random_uuid();
begin
  perform _require_admin();
  if p_email is null or position('@' in p_email) = 0 then raise exception 'Email inválido' using errcode = '22023'; end if;
  if p_password is null or length(p_password) < 6 then raise exception 'Senha deve ter ao menos 6 caracteres' using errcode = '22023'; end if;
  if p_username !~ '^[a-z0-9._-]{3,30}$' then raise exception 'Username inválido (3-30 chars, [a-z0-9._-])' using errcode = '22023'; end if;
  if exists (select 1 from app_user where username = p_username) then raise exception 'Username já em uso' using errcode = '23505'; end if;
  if exists (select 1 from auth.users where email = p_email) then raise exception 'Email já em uso' using errcode = '23505'; end if;
  if p_role = 'fornecedor' and p_supplier_id is null then raise exception 'Fornecedor exige supplier_id' using errcode = '22023'; end if;

  insert into auth.users (
    instance_id, id, aud, role, email, encrypted_password, email_confirmed_at,
    raw_app_meta_data, raw_user_meta_data, created_at, updated_at,
    confirmation_token, email_change, email_change_token_new, recovery_token
  ) values (
    '00000000-0000-0000-0000-000000000000', v_user_id, 'authenticated', 'authenticated',
    p_email, extensions.crypt(p_password, extensions.gen_salt('bf')), now(),
    jsonb_build_object('provider','email','providers',jsonb_build_array('email')),
    jsonb_build_object('full_name', p_full_name), now(), now(), '', '', '', ''
  );
  insert into auth.identities (
    id, user_id, provider_id, identity_data, provider, last_sign_in_at, created_at, updated_at
  ) values (
    gen_random_uuid(), v_user_id, v_user_id::text,
    jsonb_build_object('sub', v_user_id::text, 'email', p_email, 'email_verified', true),
    'email', now(), now(), now()
  );
  insert into app_user (
    id, username, full_name, role, supplier_id,
    access_start, access_end, monthly_authorization_limit, active
  ) values (
    v_user_id, p_username, p_full_name, p_role,
    case when p_role = 'fornecedor' then p_supplier_id else null end,
    p_access_start, p_access_end, coalesce(p_monthly_authorization_limit, 0), true
  );
  return v_user_id;
end;
$$;
revoke all on function admin_create_user(text,text,text,text,user_role,uuid,date,date,numeric) from public;
grant execute on function admin_create_user(text,text,text,text,user_role,uuid,date,date,numeric) to authenticated;

create or replace function admin_delete_user(p_user_id uuid) returns void
language plpgsql security definer set search_path = public, auth as $$
begin
  perform _require_admin();
  if p_user_id = auth.uid() then raise exception 'Você não pode excluir o próprio usuário' using errcode = '42501'; end if;
  delete from auth.users where id = p_user_id;
  if not found then raise exception 'Usuário não encontrado' using errcode = '02000'; end if;
end;
$$;
revoke all on function admin_delete_user(uuid) from public;
grant execute on function admin_delete_user(uuid) to authenticated;

create or replace function admin_set_user_password(p_user_id uuid, p_new_password text) returns void
language plpgsql security definer set search_path = public, auth as $$
begin
  perform _require_admin();
  if p_new_password is null or length(p_new_password) < 6 then
    raise exception 'Senha deve ter ao menos 6 caracteres' using errcode = '22023';
  end if;
  update auth.users
     set encrypted_password = extensions.crypt(p_new_password, extensions.gen_salt('bf')), updated_at = now()
   where id = p_user_id;
  if not found then raise exception 'Usuário não encontrado' using errcode = '02000'; end if;
end;
$$;
revoke all on function admin_set_user_password(uuid,text) from public;
grant execute on function admin_set_user_password(uuid,text) to authenticated;

create or replace function admin_list_users()
returns table (
  id uuid, username text, full_name text, role user_role, active boolean,
  supplier_id uuid, monthly_authorization_limit numeric,
  access_start date, access_end date, created_at timestamptz, email text
)
language plpgsql security definer set search_path = public, auth as $$
begin
  perform _require_admin();
  return query
    select au.id::uuid,
           au.username::text,
           au.full_name::text,
           au.role::user_role,
           au.active::boolean,
           au.supplier_id::uuid,
           au.monthly_authorization_limit::numeric,
           au.access_start::date,
           au.access_end::date,
           au.created_at::timestamptz,
           u.email::text
      from app_user au
      left join auth.users u on u.id = au.id
      order by au.created_at;
end;
$$;
revoke all on function admin_list_users() from public;
grant execute on function admin_list_users() to authenticated;

