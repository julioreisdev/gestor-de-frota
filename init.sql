create or replace function _require_admin() returns void
language plpgsql security definer set search_path = public as $$
declare v_role user_role; v_active boolean;
begin
  select role, active into v_role, v_active from app_user where id = auth.uid();
  if v_role is null then raise exception 'Sessão inválida' using errcode = '28000'; end if;
