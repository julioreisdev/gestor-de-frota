create or replace function revert_service_authorization_on_maintenance_delete() returns trigger
language plpgsql as $$
declare n integer;
begin
  if old.authorization_id is null then return old; end if;
  select count(*) into n from maintenance
    where authorization_id = old.authorization_id
      and id <> old.id and deleted_at is null;
  if n = 0 then
    update service_authorization
      set status = 'emitida'
      where id = old.authorization_id and status = 'utilizada';
  end if;
  return old;
end;
$$;
drop trigger if exists trg_maintenance_revert_auth on maintenance;
create trigger trg_maintenance_revert_auth before delete on maintenance
  for each row execute function revert_service_authorization_on_maintenance_delete();

