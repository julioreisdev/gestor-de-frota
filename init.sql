create or replace function admin_set_department_responsible(
  p_dept_id uuid,
  p_user_id uuid
) returns void
language plpgsql security definer set search_path = public as $$
declare v_old_user uuid;
begin
  perform _require_admin();
  select responsible_user_id into v_old_user from department where id = p_dept_id;
  if v_old_user is not null and v_old_user is distinct from p_user_id then
    update app_user set department_id = null where id = v_old_user and department_id = p_dept_id;
  end if;
  update department set responsible_user_id = p_user_id where id = p_dept_id;
  if not found then raise exception 'Secretaria não encontrada'; end if;
  if p_user_id is not null then
    update app_user set department_id = p_dept_id where id = p_user_id;
  end if;
end;
$$;
