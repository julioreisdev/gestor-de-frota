create or replace function emit_service_authorization(
  p_date date,
  p_vehicle_id uuid,
  p_supplier_id uuid,
  p_service_kind service_kind,
  p_description text,
  p_estimated_value numeric,
  p_responsible_name text,
  p_notes text default null
) returns uuid
language plpgsql security definer set search_path = public, auth as $$
declare
  v_auth_id uuid; v_number text;
  v_plate text; v_model text; v_dept text; v_sup_name text; v_sup_cnpj text; v_sup_kind supplier_kind;
  v_user_limit numeric; v_month_used numeric;
  v_qr text;
begin
  -- Snapshots
  select plate, model into v_plate, v_model
    from vehicle where id = p_vehicle_id and deleted_at is null;
  if v_plate is null then raise exception 'Veículo não encontrado'; end if;
  select d.acronym into v_dept
    from vehicle v left join department d on d.id = v.department_id
    where v.id = p_vehicle_id;
  select kind, coalesce(trade_name, legal_name), cnpj
    into v_sup_kind, v_sup_name, v_sup_cnpj
    from supplier where id = p_supplier_id;
  if v_sup_name is null then raise exception 'Fornecedor não encontrado'; end if;
  if v_sup_kind = 'posto' then
    raise exception 'Fornecedor cadastrado apenas como Posto não pode receber autorização de manutenção';
  end if;
  if p_estimated_value is null or p_estimated_value <= 0 then
    raise exception 'Valor estimado deve ser maior que zero';
  end if;
  if p_description is null or length(trim(p_description)) < 3 then
    raise exception 'Descrição do serviço é obrigatória (mínimo 3 caracteres)';
  end if;

  -- Limite mensal (soma combustível + manutenção)
  select monthly_authorization_limit into v_user_limit
    from app_user
    where full_name = p_responsible_name and active = true
    limit 1;
  if v_user_limit is not null and v_user_limit > 0 then
    select coalesce((
      select sum(estimated_total) from fueling_authorization
       where responsible_name = p_responsible_name
         and status in ('emitida','utilizada')
         and date_trunc('month', date) = date_trunc('month', p_date)
         and deleted_at is null
    ), 0) + coalesce((
      select sum(estimated_value) from service_authorization
       where responsible_name = p_responsible_name
         and status in ('emitida','utilizada')
         and date_trunc('month', date) = date_trunc('month', p_date)
         and deleted_at is null
    ), 0)
    into v_month_used;
