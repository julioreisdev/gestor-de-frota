    end if;
  end if;

  v_number := generate_service_authorization_number(p_date);
  v_qr := format(
    E'GERIR FROTA — AUTORIZAÇÃO DE MANUTENÇÃO\n' ||
    E'Nº: %s\n' ||
    E'Data: %s\n' ||
    E'Veículo: %s — %s\n' ||
    E'Categoria: %s\n' ||
    E'Serviço: %s\n' ||
    E'Valor estimado: R$ %s\n' ||
    E'Mecânica: %s (CNPJ %s)\n' ||
    E'Secretaria: %s\n' ||
    E'Responsável: %s\n' ||
    E'Situação: EMITIDA',
    v_number,
    to_char(p_date,'DD/MM/YYYY'),
    v_plate, v_model,
    upper(p_service_kind::text),
    p_description,
    to_char(p_estimated_value, 'FM999990D90'),
    v_sup_name, v_sup_cnpj,
    coalesce(v_dept,'—'),
    p_responsible_name
  );

  insert into service_authorization (
    number, date, vehicle_id, supplier_id, service_kind, description,
    estimated_value, responsible_name, notes,
    vehicle_plate_snapshot, vehicle_model_snapshot,
    department_acronym_snapshot, supplier_trade_name_snapshot,
