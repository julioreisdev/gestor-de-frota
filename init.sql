-- vehicle.current_km recebe o maior km_final lançado
create or replace function update_vehicle_km_from_fueling() returns trigger
language plpgsql as $$
begin
  if new.km_final is not null then
    update vehicle
      set current_km = greatest(current_km, new.km_final)
      where id = new.vehicle_id;
  end if;
  return new;
end;
$$;
create trigger trg_fueling_update_vehicle_km
  after insert or update on fueling
  for each row execute function update_vehicle_km_from_fueling();

-- TCE 517 §1.1.8: se veículo é tipo "Outros" (99), km do abastecimento = 99999999
create or replace function validate_fueling_outros_rule() returns trigger
language plpgsql as $$
declare v_type smallint;
begin
  select vehicle_type_code into v_type from vehicle where id = new.vehicle_id;
  if v_type = 99 then
    if new.km_initial is distinct from 99999999 or new.km_final is distinct from 99999999 then
      raise exception 'Veículo tipo OUTROS (99): km_initial e km_final do abastecimento devem ser 99999999';
    end if;
  else
    if new.km_initial = 99999999 or new.km_final = 99999999 then
      raise exception 'Veículo não-OUTROS não pode ter km_initial/km_final = 99999999 (sentinela reservada)';
    end if;
  end if;
  return new;
end;
$$;
create trigger trg_fueling_validate_outros
  before insert or update on fueling
  for each row execute function validate_fueling_outros_rule();

-- Abastecimento vinculado: qty ≤ autorizado, demais campos batem
create or replace function validate_fueling_against_authorization() returns trigger
language plpgsql as $$
declare
  a_qty numeric; a_veh uuid; a_sup uuid; a_fuel smallint;
begin
  if new.authorization_id is null then return new; end if;
  select authorized_quantity, vehicle_id, supplier_id, fuel_type_code
    into a_qty, a_veh, a_sup, a_fuel
    from fueling_authorization where id = new.authorization_id;
  if a_qty is null then
    raise exception 'Autorização % não encontrada', new.authorization_id;
  end if;
  if new.quantity > a_qty then
    raise exception 'Quantidade abastecida (% L) excede autorizado (% L)', new.quantity, a_qty;
  end if;
  if new.vehicle_id  <> a_veh  then raise exception 'Veículo divergente da autorização';     end if;
  if new.supplier_id <> a_sup  then raise exception 'Fornecedor divergente da autorização';  end if;
  if new.fuel_type_code <> a_fuel then raise exception 'Combustível divergente da autorização'; end if;
  return new;
end;
$$;
create trigger trg_fueling_validate_auth
  before insert or update on fueling
  for each row execute function validate_fueling_against_authorization();

-- Ao inserir fueling vinculado, autorização vira 'utilizada'
create or replace function mark_authorization_as_used() returns trigger
language plpgsql as $$
begin
  if new.authorization_id is not null then
    update fueling_authorization
      set status = 'utilizada'
      where id = new.authorization_id and status = 'emitida';
  end if;
  return new;
end;
$$;
create trigger trg_fueling_mark_auth_used
  after insert on fueling
  for each row execute function mark_authorization_as_used();

-- Ao deletar fueling vinculado, se for o último, autorização volta a 'emitida'
create or replace function revert_authorization_on_fueling_delete() returns trigger
language plpgsql as $$
declare n integer;
begin
  if old.authorization_id is null then return old; end if;
  select count(*) into n from fueling
    where authorization_id = old.authorization_id
      and id <> old.id and deleted_at is null;
  if n = 0 then
    update fueling_authorization
      set status = 'emitida'
