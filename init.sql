create or replace function emit_authorization(
  p_date date,
  p_vehicle_id uuid,
  p_supplier_id uuid,
  p_fuel_type_code smallint,
  p_quantity numeric,
  p_responsible_name text,
  p_notes text default null,
  p_fuel_subtype_id smallint default null
) returns uuid
language plpgsql security definer set search_path = public, auth as $$
declare
  v_auth_id uuid;
  v_number  text;
  v_price   numeric(8,3);
  v_contract numeric; v_balance numeric;
  v_tank    numeric;
  v_user_limit numeric; v_month_used numeric; v_estimated numeric;
  v_plate text; v_model text; v_dept text; v_sup_name text; v_sup_cnpj text; v_qr text;
  v_fuel_desc text; v_sub_desc text;
begin
  -- Snapshots do veículo
  select plate, model, tank_capacity into v_plate, v_model, v_tank
    from vehicle where id = p_vehicle_id and deleted_at is null;
  if v_plate is null then raise exception 'Veículo não encontrado'; end if;

  select d.acronym into v_dept
    from vehicle v left join department d on d.id = v.department_id
    where v.id = p_vehicle_id;

  select coalesce(trade_name, legal_name), cnpj
    into v_sup_name, v_sup_cnpj
    from supplier where id = p_supplier_id;
  if v_sup_name is null then raise exception 'Fornecedor não encontrado'; end if;

  if v_tank is not null and p_quantity > v_tank then
    raise exception 'Quantidade % L excede capacidade do tanque (% L)', p_quantity, v_tank;
  end if;

  -- Descrição do subtipo (se houver)
  if p_fuel_subtype_id is not null then
    select description into v_sub_desc from fuel_subtype where id = p_fuel_subtype_id;
    if v_sub_desc is null then
      raise exception 'Subtipo de combustível % não existe', p_fuel_subtype_id;
    end if;
  end if;

  -- Preço, saldo: busca pela combinação supplier × fuel_type × subtype
  select unit_price, contract_amount, current_balance
    into v_price, v_contract, v_balance
    from supplier_fuel
    where supplier_id = p_supplier_id
      and fuel_type_code = p_fuel_type_code
      and coalesce(fuel_subtype_id, 0::smallint) = coalesce(p_fuel_subtype_id, 0::smallint);
  if v_price is null then
    raise exception 'Fornecedor não vende esse combustível%',
      case when p_fuel_subtype_id is not null then ' (' || v_sub_desc || ')' else '' end;
  end if;
  if v_contract > 0 and p_quantity > v_balance then
    raise exception 'Saldo insuficiente: disponível % L, solicitado % L', v_balance, p_quantity;
  end if;
  v_estimated := p_quantity * v_price;

  -- Limite mensal do responsável
  select monthly_authorization_limit into v_user_limit
    from app_user
    where full_name = p_responsible_name and active = true
    limit 1;
  if v_user_limit is not null and v_user_limit > 0 then
    select coalesce(sum(estimated_total),0) into v_month_used
      from fueling_authorization
      where responsible_name = p_responsible_name
        and status in ('emitida','utilizada')
        and date_trunc('month', date) = date_trunc('month', p_date)
        and deleted_at is null;
    if v_month_used + v_estimated > v_user_limit then
      raise exception 'Limite mensal do responsável excedido: limite R$%, usado R$%, nova R$%',
        v_user_limit, v_month_used, v_estimated;
    end if;
  end if;

  -- Número da autorização + payload do QR (texto rico, escaneável)
  v_number := generate_authorization_number(p_date);
  select description into v_fuel_desc from fuel_type where code = p_fuel_type_code;

  v_qr := format(
    E'GERIR FROTA — AUTORIZAÇÃO DE ABASTECIMENTO\n' ||
    E'Nº: %s\n' ||
    E'Data: %s\n' ||
    E'Veículo: %s — %s\n' ||
    E'Combustível: %s%s\n' ||
    E'Quantidade: %s L\n' ||
    E'Valor estimado: R$ %s\n' ||
    E'Fornecedor: %s (CNPJ %s)\n' ||
    E'Secretaria: %s\n' ||
    E'Responsável: %s\n' ||
    E'Situação: EMITIDA',
    v_number,
    to_char(p_date,'DD/MM/YYYY'),
    v_plate, v_model,
    v_fuel_desc,
    case when v_sub_desc is not null then ' / ' || v_sub_desc else '' end,
    to_char(p_quantity, 'FM999990D90'),
    to_char(v_estimated, 'FM999990D90'),
    v_sup_name, v_sup_cnpj,
    coalesce(v_dept, '—'),
    p_responsible_name
  );

  insert into fueling_authorization (
    number, date, vehicle_id, supplier_id, fuel_type_code, fuel_subtype_id,
    authorized_quantity, unit_price_snapshot, responsible_name, notes,
    vehicle_plate_snapshot, vehicle_model_snapshot,
    department_acronym_snapshot, supplier_trade_name_snapshot,
    qr_payload, created_by
  ) values (
    v_number, p_date, p_vehicle_id, p_supplier_id, p_fuel_type_code, p_fuel_subtype_id,
    p_quantity, v_price, p_responsible_name, p_notes,
    v_plate, v_model, v_dept, v_sup_name,
    v_qr, auth.uid()
  ) returning id into v_auth_id;

  -- Debita saldo
  if v_contract > 0 then
    update supplier_fuel
      set current_balance = current_balance - p_quantity
      where supplier_id = p_supplier_id
        and fuel_type_code = p_fuel_type_code
        and coalesce(fuel_subtype_id, 0::smallint) = coalesce(p_fuel_subtype_id, 0::smallint);
  end if;

  return v_auth_id;
end;
$$;
revoke all on function emit_authorization(date,uuid,uuid,smallint,numeric,text,text,smallint) from public;
grant execute on function emit_authorization(date,uuid,uuid,smallint,numeric,text,text,smallint) to authenticated;


create or replace function cancel_authorization(p_auth_id uuid) returns void
language plpgsql security definer set search_path = public, auth as $$
declare v_sup uuid; v_fuel smallint; v_subtype smallint; v_qty numeric; v_contract numeric;
begin
  select supplier_id, fuel_type_code, fuel_subtype_id, authorized_quantity
    into v_sup, v_fuel, v_subtype, v_qty
    from fueling_authorization
    where id = p_auth_id and status = 'emitida' and deleted_at is null;
  if v_sup is null then raise exception 'Autorização não encontrada ou não está emitida'; end if;

  update fueling_authorization set status = 'cancelada' where id = p_auth_id;

  select contract_amount into v_contract from supplier_fuel
    where supplier_id = v_sup
      and fuel_type_code = v_fuel
      and coalesce(fuel_subtype_id, 0::smallint) = coalesce(v_subtype, 0::smallint);
  if v_contract > 0 then
    update supplier_fuel
      set current_balance = least(contract_amount, current_balance + v_qty)
      where supplier_id = v_sup
        and fuel_type_code = v_fuel
        and coalesce(fuel_subtype_id, 0::smallint) = coalesce(v_subtype, 0::smallint);
  end if;
end;
$$;


create or replace function update_authorization_quantity(p_auth_id uuid, p_new_qty numeric) returns void
language plpgsql security definer set search_path = public, auth as $$
declare
  v_sup uuid; v_fuel smallint; v_subtype smallint;
  v_old numeric; v_unit_price numeric; v_contract numeric; v_balance numeric; v_diff numeric;
begin
  if p_new_qty <= 0 then raise exception 'Quantidade deve ser > 0'; end if;

  select supplier_id, fuel_type_code, fuel_subtype_id, authorized_quantity, unit_price_snapshot
    into v_sup, v_fuel, v_subtype, v_old, v_unit_price
    from fueling_authorization
    where id = p_auth_id and status = 'emitida' and deleted_at is null;
  if v_sup is null then raise exception 'Autorização não encontrada ou não está emitida'; end if;

  v_diff := p_new_qty - v_old;
  if v_diff = 0 then return; end if;

  select contract_amount, current_balance into v_contract, v_balance
    from supplier_fuel
    where supplier_id = v_sup
      and fuel_type_code = v_fuel
      and coalesce(fuel_subtype_id, 0::smallint) = coalesce(v_subtype, 0::smallint);

  if v_contract > 0 and v_diff > 0 and v_diff > v_balance then
    raise exception 'Saldo insuficiente para aumento: disponível % L', v_balance;
  end if;

  update fueling_authorization
    set authorized_quantity = p_new_qty
    where id = p_auth_id;

  if v_contract > 0 then
    update supplier_fuel
      set current_balance = least(contract_amount, current_balance - v_diff)
      where supplier_id = v_sup
        and fuel_type_code = v_fuel
        and coalesce(fuel_subtype_id, 0::smallint) = coalesce(v_subtype, 0::smallint);
  end if;
end;
$$;


create or replace function delete_authorization(p_auth_id uuid) returns void
language plpgsql security definer set search_path = public, auth as $$
declare
  v_status authorization_status; v_sup uuid; v_fuel smallint; v_subtype smallint;
  v_qty numeric; v_contract numeric; v_used_count integer;
begin
  select status, supplier_id, fuel_type_code, fuel_subtype_id, authorized_quantity
    into v_status, v_sup, v_fuel, v_subtype, v_qty
    from fueling_authorization
    where id = p_auth_id and deleted_at is null;
  if v_status is null then raise exception 'Autorização não encontrada'; end if;

  -- Bloqueia delete se há abastecimentos vinculados
  select count(*) into v_used_count from fueling
    where authorization_id = p_auth_id and deleted_at is null;
  if v_used_count > 0 then
    raise exception 'Não é possível excluir: % abastecimento(s) vinculado(s) a esta autorização.', v_used_count;
  end if;

  -- Se ainda emitida, restaura saldo antes de deletar
  if v_status = 'emitida' then
    select contract_amount into v_contract from supplier_fuel
      where supplier_id = v_sup
        and fuel_type_code = v_fuel
        and coalesce(fuel_subtype_id, 0::smallint) = coalesce(v_subtype, 0::smallint);
    if v_contract > 0 then
      update supplier_fuel
        set current_balance = least(contract_amount, current_balance + v_qty)
        where supplier_id = v_sup
          and fuel_type_code = v_fuel
          and coalesce(fuel_subtype_id, 0::smallint) = coalesce(v_subtype, 0::smallint);
    end if;
  end if;

  delete from fueling_authorization where id = p_auth_id;
end;
$$;
